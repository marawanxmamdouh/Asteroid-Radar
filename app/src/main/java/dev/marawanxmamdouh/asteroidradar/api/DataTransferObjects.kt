package dev.marawanxmamdouh.asteroidradar.api

import com.squareup.moshi.JsonClass
import dev.marawanxmamdouh.asteroidradar.database.AsteroidEntity
import dev.marawanxmamdouh.asteroidradar.model.Asteroid

@JsonClass(generateAdapter = true)
class NetworkAsteroidContainer(val asteroids: ArrayList<NetworkAsteroid>)

data class NetworkAsteroid(
    val id: Long, val codename: String, val closeApproachDate: String,
    val absoluteMagnitude: Double, val estimatedDiameter: Double,
    val relativeVelocity: Double, val distanceFromEarth: Double,
    val isPotentiallyHazardous: Boolean
)


/**
 * Convert [NetworkAsteroid] results to [Asteroid] objects
 */
fun NetworkAsteroidContainer.asDomainModel(): List<Asteroid> {
    return asteroids.map {
        Asteroid(
            id = it.id,
            codename = it.codename,
            closeApproachDate = it.closeApproachDate,
            absoluteMagnitude = it.absoluteMagnitude,
            estimatedDiameter = it.estimatedDiameter,
            relativeVelocity = it.relativeVelocity,
            distanceFromEarth = it.distanceFromEarth,
            isPotentiallyHazardous = it.isPotentiallyHazardous
        )
    }
}

/**
 * Converts from [NetworkAsteroid] objects to [AsteroidEntity] objects
 */
fun NetworkAsteroidContainer.asDatabaseModel(): Array<AsteroidEntity> {
    return asteroids.map {
        AsteroidEntity(
            id = it.id,
            codename = it.codename,
            closeApproachDate = it.closeApproachDate,
            absoluteMagnitude = it.absoluteMagnitude,
            estimatedDiameter = it.estimatedDiameter,
            relativeVelocity = it.relativeVelocity,
            distanceFromEarth = it.distanceFromEarth,
            isPotentiallyHazardous = it.isPotentiallyHazardous
        )
    }.toTypedArray()
}